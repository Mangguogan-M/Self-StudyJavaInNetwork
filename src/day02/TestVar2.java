package day02;

/*
	1.声明变量age存储你的年龄   输出在控制台上
	3.声明变量name用于存储你的姓名  输出在控制台上
*/
public class TestVar2 {

	public static void main(String[] args) {

		// 声明一个int类型的变量age 来存储年龄 整数
		int age = 18;
		// 输出age 变量
		System.out.println(age);
		// 声明一个String类型的变量 name 来存储姓名 字符串
		String name = "李白";
		// 输出name变量
		System.out.println(name);
	}
}